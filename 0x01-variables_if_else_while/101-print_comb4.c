#include <stdlib.h>
#include <stdio.h>
#include <string.h>
#include <ctype.h>
/**
 *main - Entry point
 *
 *Description: Positive anything is better than negative nothing
 *
 *Return: Always 0 (Success)
 */

int main(void)
{
	int a, b, c;

	for (a = '0' ; a <= '7' ; a++)
	{
		for (b = b + 1 ; b <= '8' ; b++)
		{
			for (c = c + 1 ; c <= '9' ; c++)
			{
				putchar(a);
				putchar(b);
				putchar(c);

				if ((a == '7') && (b == '8') && (c == '9'))
				{
					putchar('\n');
				}
				else
				{
					putchar(',');
					putchar(' ');
				}
			}
		}
	}
	return (0);
}
